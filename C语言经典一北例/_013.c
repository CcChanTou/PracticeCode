/*
题目：打印出所有的"水仙花数"，所谓"水仙花数"是指一个三位数，其各位数字立方和等于该数 本身。例如：153是一个"水仙花数"，因为153=1的三次方＋5的三次方＋3的三次方。  
*/
#include <stdio.h>
int main()
{
    int i, j, k;
    for (int x=100; x<1000; x++)
    {
        i = x/100%10;
        j = x/10%10;
        k = x%10;
        if (x == i*i*i + j*j*j + k*k*k)
        {
            printf("%d\n", x);
        }
    }
    return 0;
}
