/* 说明：老程序是每次将x右移，判断最右边的位数是否为1，是就将b+1 */
/* 新的程序是每次将最右边值为1的二进制位删除，相当于直接跑到有1的位置并右移，然后将b+1 */
int bitcount(unsigned x)
{
    int b;
    /* 
    old:
    for (b = 0; x!=0; x>>1)
    {
        if (x&01)
        {
            b++;
        }
    }
    */
    for (b = 0; x!= 0; x &= x-1)
    {
        b++;
    }
    return b;
}