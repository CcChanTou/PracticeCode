/* setbits(s, p, n, y) 将x中从第p位开始的n个二进制位设置为y中最右边n位的值，x的其余各位保持不变 */
/* 总体思想对x和y处理然后或处理 */
/* 1.对y操作，获取y中最右边的值并移位到对应x中要操作的位置--> (y & ~(~0 << n)) << (p+1-n)*/
/* 2.对x操作，因为最终是将x，y或操作，此时对x的处理是将需要处理的位置置0--> x & ~(~(~0 << n) << (p+1-n))*/
unsigned setbits(unsigned x, int p, int n, unsigned y)
{
    return x & ~(~(~0 << n) << (p+1-n)) | (y & ~(~0 << n)) << (p+1-n);
}




